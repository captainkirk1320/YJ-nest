/**
 * Build a credit-export xlsx mirroring the 2026-05-22 production v2.0 shape:
 *   - R2 title, R3 generated-by
 *   - R9: left-block close-date filter (Custom (MM/DD/YYYY to MM/DD/YYYY))
 *   - R11: left-block team filter (comma-joined)
 *   - R14: right-block planned-start filter
 *   - R16: right-block team filter
 *   - R19: block-label row
 *   - R20: header row
 *   - R21+: data rows
 */
function buildCreditXlsx(args: {
  outPath: string;
  title: string;
  opportunitiesDateRange: string; // 'MM/DD/YYYY to MM/DD/YYYY'
  pointsDateRange?: string;
  teamFilterLeft: string;   // comma-joined
  teamFilterRight?: string; // defaults to teamFilterLeft
  leftRows: Array<{ fcid: string; name: string; opp: string; amt: number; type: string }>;
  rightRows?: Array<{ fcid: string; name: string; job: string; pts: number; campaign: string }>;
}): void {
  const grid: unknown[][] = [];
  grid.push([]);                                                                                       // R1
  grid.push([null, args.title]);                                                                       // R2
  grid.push([null, `As of 2026-05-22 11:40:54 MST • Generated by test`]);                              // R3
  grid.push([]); grid.push([]);                                                                        // R4 R5
  grid.push([null, 'Filtered By']);                                                                    // R6
  grid.push([null, 'Yellow Jacket Credit- Opportunities']);                                            // R7
  grid.push([null, 'Show: All accounts']);                                                             // R8
  grid.push([null, `Date Field: Opportunity: Close Date equals Custom (${args.opportunitiesDateRange})`]); // R9
  grid.push([null, 'Account Name contains Yellow Jacket']);                                            // R10
  grid.push([null, `Yellow Jacket Team equals ${args.teamFilterLeft}`]);                               // R11
  grid.push([null, 'Volunteer Points']);                                                               // R12
  grid.push([null, 'Show: All accounts']);                                                             // R13
  grid.push([null, `Date Field: Planned Start Date & Time equals Custom (${args.pointsDateRange ?? args.opportunitiesDateRange})`]); // R14
  grid.push([null, 'Account Name contains Yellow Jacket']);                                            // R15
  grid.push([null, `Yellow Jacket Team equals ${args.teamFilterRight ?? args.teamFilterLeft}`]);       // R16
  grid.push([]);                                                                                       // R17
  grid.push([null, 'Contacts with Custom Object', null, null, null, null, 'Contacts with Custom Object']); // R18
  grid.push([null, 'Yellow Jacket Credit- Opportunities', null, null, null, null, 'Volunteer Points']);// R19
  grid.push([]);                                                                                       // R20 separator
  // Header — production v2 column order: Contact Full Name | Full Contact ID | ...
  grid.push([
    null,
    'Contact Full Name', null, 'Full Contact ID', 'Opportunity: Opportunity Name', 'Amount Credited', 'Type',
    'Contact Full Name', 'Full Contact ID', 'Volunteer Job: Volunteer Job Name', 'Volunteer Points', 'Volunteer Job: Campaign',
  ]);

  const right = args.rightRows ?? [];
  const maxRows = Math.max(args.leftRows.length, right.length);
  for (let i = 0; i < maxRows; i++) {
    const l = args.leftRows[i];
    const r = right[i];
    grid.push([
      null,
      l?.name ?? null, null, l?.fcid ?? null, l?.opp ?? null, l?.amt ?? null, l?.type ?? null,
      r?.name ?? null, r?.fcid ?? null, r?.job ?? null, r?.pts ?? null, r?.campaign ?? null,
    ]);
  }
  grid.push([null, 'Total', null, null, null, 0, null, null, null, null, 0, null]);
  grid.push([null, 'Confidential Information - Do Not Distribute']);

  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, XLSX.utils.aoa_to_sheet(grid), 'Sheet1');
  XLSX.writeFile(wb, args.outPath);
}

