describe('Multi-team filter parsing', () => {
  it('extracts both teams from filter rows 11 + 16 (union)', () => {
    const path = resolve(TMP, '2026-27 Active YJ - 1.0-2026-05-22-12-00-00.xlsx');
    buildCreditXlsx({
      outPath: path,
      title: '2026-27 Active YJ - 1.0',
      opportunitiesDateRange: '1/1/2026 to 12/31/2026',
      teamFilterLeft: 'Cobbers,Fighting Camels',
      leftRows: [
        { fcid: 'C001', name: 'Alice Acorn', opp: '2025 FKE Paddle Raise', amt: 600, type: 'Cash' },
      ],
    });
    const roster = makeRoster([
      row({ full_contact_id: 'C001', sales_rep_id: 1001, full_name: 'Alice Acorn', team: 'Cobbers' }),
    ]);
    const errors = new IngestErrorCollector();
    const result = parseVolunteerCredit({
      creditPath: path,
      roster,
      cashRoutingAllowlist: SEEDS_BASE,
      routingPatterns: PATTERNS,
      errors,
    });
    expect(result.metadata.teams_in_filter).toEqual(['Cobbers', 'Fighting Camels']);
    // Date range parsed.
    expect(result.metadata.opportunities_date_range).toEqual({ start: '2026-01-01', end: '2026-12-31' });
    // No team-disagreement warning (row 11 and row 16 match).
    expect(errors.all().find((e) => e.kind === 'credit_filter_team_disagreement')).toBeUndefined();
    rmSync(path);
  });

  it('emits credit_filter_team_disagreement when row 11 ≠ row 16, uses union', () => {
    const path = resolve(TMP, 'disagree.xlsx');
    buildCreditXlsx({
      outPath: path,
      title: '2026-27 Active YJ - 1.0',
      opportunitiesDateRange: '1/1/2026 to 12/31/2026',
      teamFilterLeft: 'Cobbers',
      teamFilterRight: 'Fighting Camels',
      leftRows: [
        { fcid: 'C001', name: 'Alice', opp: '2025 FKE Paddle Raise', amt: 100, type: 'Cash' },
      ],
    });
    const roster = makeRoster([
      row({ full_contact_id: 'C001', sales_rep_id: 1, full_name: 'Alice', team: 'Cobbers' }),
    ]);
    const errors = new IngestErrorCollector();
    const result = parseVolunteerCredit({
      creditPath: path, roster, cashRoutingAllowlist: SEEDS_BASE, routingPatterns: PATTERNS, errors,
    });
    expect(result.metadata.teams_in_filter.sort()).toEqual(['Cobbers', 'Fighting Camels']);
    const disagree = errors.all().filter((e) => e.kind === 'credit_filter_team_disagreement');
    expect(disagree.length).toBe(1);
    rmSync(path);
  });
});

